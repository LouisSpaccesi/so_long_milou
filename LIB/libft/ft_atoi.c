int	ft_atoi(const char *s)
{
	int	sign;
	int	number;

	while (((*s >= 9 && *s <= 13) || (*s == ' ')) && (*s))
		s++;
	sign = 1;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	number = 0;
	while (*s >= '0' && *s <= '9' && *s)
	{
		number = number * 10 + *s - '0';
		s++;
	}
	return (number * sign);
}
